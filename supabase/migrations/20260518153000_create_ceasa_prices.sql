create or replace function find_nearest_ceasa(
    lat double precision,
    lng double precision
)
returns table (
    id uuid,
    name text,
    city text,
    uf text,
    distance_meters double precision
)
language sql
as $$
select
    c.id,
    c.name,
    c.city,
    c.uf,
    ST_Distance(
        c.location,
        ST_SetSRID(
            ST_MakePoint(lng, lat),
            4326
        )::geography
    ) as distance_meters
from ceasas c
order by
    c.location <->
    ST_SetSRID(
        ST_MakePoint(lng, lat),
        4326
    )::geography
limit 1;
$$;